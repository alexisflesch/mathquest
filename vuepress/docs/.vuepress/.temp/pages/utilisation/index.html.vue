<h2 id="fonctionnalites-principales" tabindex="-1"><a class="header-anchor" href="#fonctionnalites-principales"><span>Fonctionnalités principales</span></a></h2>
<p>MathQuest propose trois modes de jeu principaux :</p>
<ul>
<li><strong>Quiz (mode enseignant)</strong> : Animez un quiz en temps réel avec vos élèves. Vous contrôlez l’enchaînement des questions, le timer, et la diffusion des réponses ou corrections. Les élèves répondent en direct, et vous choisissez quand donner le feedback (oralement ou à la fin).</li>
<li><strong>Tournoi (mode compétition)</strong> : Organisez un tournoi synchrone (tout le monde joue en même temps) ou différé (chacun peut rejouer la partie à son rythme). Le système gère le timer, la progression, le classement et affiche les bonnes réponses et feedbacks à la fin de chaque question.</li>
<li><strong>Entraînement (mode libre)</strong> : Entraînez-vous en solo, sans timer ni pression. Vous enchaînez les questions à votre rythme, recevez un feedback immédiat après chaque réponse, et pouvez recommencer autant de fois que vous le souhaitez. Aucun score n’est enregistré.</li>
</ul>
<h2 id="prise-en-main-rapide" tabindex="-1"><a class="header-anchor" href="#prise-en-main-rapide"><span>Prise en main rapide</span></a></h2>
<ol>
<li>
<p><strong>Connexion à l'application</strong></p>
<ul>
<li>Choisissez votre profil (élève, enseignant ou invité) à la connexion. En mode invité, pas besoin de créer un compte : choisissez simplement un pseudo et un avatar, et vous êtes prêt à jouer. Vos résultats ne seront pas sauvegardés, mais vous pourrez créer un compte plus tard si besoin.</li>
<li>Si vous êtes enseignant, créez un compte pour gérer vos activités : regroupez des questions, créez des quiz, des tournois ou des sessions d’entraînement pour vos élèves.</li>
</ul>
</li>
<li>
<p><strong>Déroulement selon le mode</strong></p>
<ul>
<li><strong>Quiz</strong> : Créez ou sélectionnez un quiz, invitez vos élèves à rejoindre, puis pilotez le déroulement de la session.</li>
<li><strong>Tournoi</strong> : Lancez un tournoi (synchrone ou différé), partagez le code d’accès, et laissez le système gérer la progression et le classement.</li>
<li><strong>Entraînement</strong> : Démarrez une session d’entraînement libre, répondez à des questions à votre rythme, et obtenez un feedback immédiat.</li>
</ul>
</li>
</ol>
